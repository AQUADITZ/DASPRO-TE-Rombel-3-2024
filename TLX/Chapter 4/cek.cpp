#include <iostream>
using namespace std;

int main() {
    int x = 10;

    if (x > 10) {
        cout << "nilai x lebih dari 10" << endl;
    }
    if (x <= 10) {
        cout << "nilai x kurang dari atau sama dengan 10" << endl;
    }
    if (x >= 9) {
        cout << "nilai x lebih dari atau sama dengan 9" << endl;
    }
}

